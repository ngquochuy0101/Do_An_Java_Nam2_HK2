    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        try {
            String idHoaDon = tfTimKiem.getText();
            String batDau = tfBatDau.getText();
            String ketThuc = tfKetThuc.getText();

            if (idHoaDon.isEmpty() && batDau.isEmpty() && ketThuc.isEmpty()) {
                // Hiển thị thông báo lỗi khi trường nhập liệu rỗng
                JOptionPane.showMessageDialog(this, "Vui lòng nhập thông tin", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (!idHoaDon.isEmpty()) {
                try {
                    int idHD = Integer.parseInt(idHoaDon);
                    ArrayList<HoaDonDTO> dsHoaDon = thongKeAdminBUS.timIdHoaDon(idHD);
                    capNhatBangHoaDon(dsHoaDon);
                    tfTimKiem.setText("");
                    tfBatDau.setText("");
                    tfKetThuc.setText("");
                    return; // Chỉ tìm kiếm theo mã hóa đơn, kết thúc sau khi tìm
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Mã hóa đơn phải là một số nguyên", "Lỗi", JOptionPane.ERROR_MESSAGE);
                    tfTimKiem.requestFocus();
                    return;
                }
            }

            if (!batDau.isEmpty() && !ketThuc.isEmpty()) {
                // Kiểm tra định dạng ngày tháng nhập vào
                SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
                try {
                    Date startDate = dateFormat.parse(batDau);
                    Date endDate = dateFormat.parse(ketThuc);

                    if (startDate.after(endDate)) {
                        JOptionPane.showMessageDialog(this, "Ngày bắt đầu không được lớn hơn ngày kết thúc", "Lỗi", JOptionPane.ERROR_MESSAGE);
                        tfBatDau.requestFocus();
                        return;
                    }
                    if (!thongKeAdminBUS.isValidDate(batDau) || !thongKeAdminBUS.isValidDate(ketThuc)) {
                        JOptionPane.showMessageDialog(this, "Ngày hoặc tháng không hợp lệ. Ngày phải từ 1-31, tháng phải từ 1-12", "Lỗi", JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                    ArrayList<HoaDonDTO> dsHoaDon = thongKeAdminBUS.timHoaDonTheoNgay(batDau, ketThuc);
                    capNhatBangHoaDon(dsHoaDon);
                    tfTimKiem.setText("");
                } catch (ParseException e) {
                    JOptionPane.showMessageDialog(this, "Định dạng ngày tháng không hợp lệ. Vui lòng nhập ngày theo định dạng dd-MM-yyyy", "Lỗi", JOptionPane.ERROR_MESSAGE);
                    tfBatDau.requestFocus();
                }
            } else if (!batDau.isEmpty() || !ketThuc.isEmpty()) {
                // Nếu chỉ có một trong hai trường ngày được điền
                JOptionPane.showMessageDialog(this, "Vui lòng điền cả ngày bắt đầu và ngày kết thúc", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi khi tìm kiếm hóa đơn", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnTimActionPerformed

