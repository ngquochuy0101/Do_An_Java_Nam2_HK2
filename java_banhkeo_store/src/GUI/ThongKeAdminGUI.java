    public void capNhatBangHoaDon(ArrayList<HoaDonDTO> dsHoaDon) {
        // Thiết lập table
        DefaultTableModel model = (DefaultTableModel) table2.getModel();

        table2.setEnabled(false); // Chặn người dùng thao tác với table
        model.setColumnCount(0);
        model.setRowCount(0);
        model.addColumn("ID Hóa Đơn");
        model.addColumn("ID Nhân Viên");
        model.addColumn("ID Khách Hàng");
        model.addColumn("Ngày Thành Lập");
        model.addColumn("Tổng Tiền");

        // Thêm dữ liệu từ danh sách HoaDonDTO vào model
        for (HoaDonDTO HoaDon : dsHoaDon) {
            model.addRow(new Object[]{HoaDon.getIdHoaDon(), HoaDon.getIdNhanVien(), HoaDon.getIdKhachHang(), HoaDon.getNgayLap(), HoaDon.getTongTien()});
        }

        // Tạo một DefaultTableCellRenderer và đặt thuộc tính căn giữa
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(SwingConstants.CENTER);

        // Đặt renderer cho từng cột trong JTable
        for (int i = 0; i < table2.getColumnCount(); i++) {
            table2.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
        }
        table2.setModel(model);
    }

