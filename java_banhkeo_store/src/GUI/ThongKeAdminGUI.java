    private void cbxYearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxYearActionPerformed
        // Đặt lại giá trị mặc định của table
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        model.setColumnCount(0);
        try {
            String selectedYear = (String) cbxYear.getSelectedItem();
            if ("Năm".equals(selectedYear)) {
                cbxYear.hidePopup();
                textTongDoanhThu.setVisible(false);
            } else {
                int nam = Integer.parseInt(selectedYear);
                textTongDoanhThu.setVisible(true);
                DefaultTableModel tableModel = (DefaultTableModel) table.getModel();
                tableModel.setRowCount(1); // Đặt số hàng cho bảng
                tableModel.setColumnCount(5); // Đặt số cột cho bảng
                table.setEnabled(false); // Chặn người dùng sử dụng table

                // Lấy tổng doanh thu của từng quý trong năm và thêm dữ liệu vào tableModel
                ArrayList<Integer> doanhThuTheoQuy = thongKeAdminBUS.thongKeTheoQuy(nam);
                int tongDoanhThuNam = thongKeAdminBUS.tinhTongDoanhThuNam(nam);

                // Cập nhật tiêu đề cho các cột
//                tableModel.addColumn("Quý");
                table.getColumnModel().getColumn(0).setHeaderValue("Quý");
                table.getColumnModel().getColumn(1).setHeaderValue("Quý 1");
                table.getColumnModel().getColumn(2).setHeaderValue("Quý 2");
                table.getColumnModel().getColumn(3).setHeaderValue("Quý 3");
                table.getColumnModel().getColumn(4).setHeaderValue("Quý 4");

                // Thiết lập giá trị cho từng ô cụ thể
                table.setValueAt("Doanh Thu", 0, 0);
                table.setValueAt(doanhThuTheoQuy.get(0), 0, 1);
                table.setValueAt(doanhThuTheoQuy.get(1), 0, 2);
                table.setValueAt(doanhThuTheoQuy.get(2), 0, 3);
                table.setValueAt(doanhThuTheoQuy.get(3), 0, 4);

                // Thiết lập giá trị cho tổng doanh thu
                textTongDoanhThu.setText("Tổng doanh thu: " + String.valueOf(tongDoanhThuNam));

                // Tạo một DefaultTableCellRenderer và đặt thuộc tính căn giữa
                DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
                centerRenderer.setHorizontalAlignment(SwingConstants.CENTER);

                // Đặt renderer cho từng cột trong JTable
                for (int i = 0; i < table.getColumnCount(); i++) {
                    table.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_cbxYearActionPerformed

